/** Returns the cells occupied by a piece as [col, row] pairs */
export function getPieceCells(piece: Piece): [number, number][] {
  const shape = TETROMINOES[piece.type][piece.rotation];
  const cells: [number, number][] = [];
  for (let row = 0; row < shape.length; row++) {
    for (let col = 0; col < shape[row].length; col++) {
      if (shape[row][col]) {
        cells.push([piece.x + col, piece.y + row]);
      }
    }
  }
  return cells;
}

/** Spawn position for a piece type */
export function spawnPiece(type: PieceType): Piece {
  return {
    type,
    rotation: 0,
    x: type === 'O' ? 3 : 3,
    y: 0,
  };
}

